#[derive(Copy, Clone, Eq, PartialEq, Debug, Default)]
pub struct Style {
    pub foreground: Rgba,
    pub background: Rgba,
    pub bold: bool,
    pub italic: bool,
    pub underline: bool,
    pub strike: bool,
}
